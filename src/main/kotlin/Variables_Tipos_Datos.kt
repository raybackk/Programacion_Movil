const val PI=3.1416
fun main() {
    val floatDef=4.21
    println("Variable flotante por defecto $floatDef")
    val flotante:Float=3.21f
    println("Variable decimal convertida a flotante: $flotante")
    val resFloat=PI*(flotante*flotante)
    println("El perimetro del criculo  es: $resFloat centimetros cuadrados")
}